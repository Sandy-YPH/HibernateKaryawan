    private void initTable() {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("NIP");
        model.addColumn("Nama");
        model.addColumn("Jabatan");
        model.addColumn("Gaji");
        tblKaryawan.setModel(model);
    }
    
    private void loadData() {
    // Kosongkan table dulu
        model.setRowCount(0);

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        try {
            // Query semua data Karyawan
            @SuppressWarnings("unchecked")
            List<Karyawan> listKaryawan = session.createQuery("FROM Karyawan").list();

            // Tambahkan data ke table
            for (Karyawan kry : listKaryawan) {
                Object[] row = {
                    kry.getId(),
                    kry.getNip(),
                    kry.getNama(),
                    kry.getJabatan(),
                    kry.getGaji()
                };
                model.addRow(row);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error load data: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            session.close();
        }
    }
    
