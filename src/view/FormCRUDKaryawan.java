    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtNip.getText().trim().isEmpty() || 
            txtNama.getText().trim().isEmpty() || 
            txtJabatan.getText().trim().isEmpty() ||
            txtGaji.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, 
                "Semua field harus diisi!",
                "Validasi", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi gaji harus angka
        double gajiValue;
        try {
            gajiValue = Double.parseDouble(txtGaji.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Gaji harus berupa angka!",
                "Validasi",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;

        try {
            transaction = session.beginTransaction();

            Karyawan kry = new Karyawan();
            kry.setNip(txtNip.getText().trim());
            kry.setNama(txtNama.getText().trim());
            kry.setJabatan(txtJabatan.getText().trim());
            kry.setGaji(gajiValue);

            session.save(kry);
            transaction.commit();

            JOptionPane.showMessageDialog(this,
                "Data berhasil disimpan!",
                "Sukses",
                JOptionPane.INFORMATION_MESSAGE);

            loadData();
            clearForm();

        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }
            JOptionPane.showMessageDialog(this,
                "Error simpan data: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            session.close();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Pilih data dari tabel terlebih dahulu!", 
                "Validasi",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi text field
        if (txtNip.getText().trim().isEmpty() || 
            txtNama.getText().trim().isEmpty() || 
            txtJabatan.getText().trim().isEmpty() ||
            txtGaji.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this,
                "Semua field harus diisi!",
                "Validasi",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi gaji angka
        double gajiValue;
        try {
            gajiValue = Double.parseDouble(txtGaji.getText().trim());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                "Gaji harus berupa angka!",
                "Validasi",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;

        try {
            transaction = session.beginTransaction();

            int id = Integer.parseInt(txtId.getText().trim());

            // Ambil Karyawan dari database
            Karyawan kry = (Karyawan) session.get(Karyawan.class, id);

            if (kry != null) {

                // Update data
                kry.setNip(txtNip.getText().trim());
                kry.setNama(txtNama.getText().trim());
                kry.setJabatan(txtJabatan.getText().trim());
                kry.setGaji(gajiValue);

                session.update(kry);
                transaction.commit();

                JOptionPane.showMessageDialog(this,
                    "Data berhasil diupdate!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

                loadData();
                clearForm();

            } else {
                JOptionPane.showMessageDialog(this,
                    "Data tidak ditemukan!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }

            JOptionPane.showMessageDialog(this,
                "Error update data: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();

        } finally {
            session.close();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Pilih data dari tabel terlebih dahulu!", 
                "Validasi",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Konfirmasi hapus
        int confirm = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus data ini?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        Session session = NewHibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;

        try {
            transaction = session.beginTransaction();

            int id = Integer.parseInt(txtId.getText().trim());

            // Ambil data Karyawan dari DB
            Karyawan kry = (Karyawan) session.get(Karyawan.class, id);

            if (kry != null) {
                session.delete(kry);
                transaction.commit();

                JOptionPane.showMessageDialog(this,
                    "Data berhasil dihapus!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

                loadData();
                clearForm();

            } else {
                JOptionPane.showMessageDialog(this,
                    "Data tidak ditemukan!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }

            JOptionPane.showMessageDialog(this,
                "Error hapus data: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();

        } finally {
            session.close();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

